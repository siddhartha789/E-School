<h1>eSchool Lunch</h1></div>
eSchool Lunch allows your School System to collect online prepayments for Food Services. Our online</br>
services provide cost-effective solutions that are designed to save time and keep your cafeteria lines moving</br>
quickly!

With eSchool Lunch, your School System can reduce cash handling, increase participation and reduce the</br>
amount of time spent processing payments. In addition, parents will have a secure and convenient way to</br>
prepay for their student’s meals online, view balances, account history, and receive email notifications when</br> 
balances are nearing low.</br>

eSchool Lunch Benefits</br>

To the School System:</br>
• Cost-effective hardware and software options</br>
• Reduce cash/coin handling</br>
• Improve line speed</br>
• Simplify free/reduced meal tracking</br>
• Minimize phone calls to School System in regards to student balance</br>
• Reduce the number of payments staff processes</br>
• All prepayments made online are deposited directly into the School Systems bank account</br>
• Streamline paperwork by automating every-day manual reporting and tasks</br>
• Reduce the amount of notes or reminders being sent home for parents to send money</br>
• Benefit from MCC / Invoice Cloud’s friendly and knowledgeable Support</br>

To Parents:</br>
• 24/7 access</br>
• Make payments via ACH (electronic check)</br>
• Makes payments via MasterCard, American Express, Discover Card</br>
• Receive low-balance email reminders</br>
• Receive confirmation of payments via email</br>
• Make payments for multiple students in one or more schools within District</br>
• View or monitor purchases made in the cafeteria</br>
• Online payment history</br>
• Reduce lost or stolen lunch money</br>
• Benefit from MCC / Invoice Cloud’s friendly and knowledgeable Support</br>

