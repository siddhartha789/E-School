What is eSchool?</br></br>

eSchool is a feature-rich collection application, an easy to use web-based software tool that allows a school</br></br> 
system to manage, track and report various types of school funds from transportation, tuition, to athletics;</br></br>
any school fee or due.</br></br>

With the growing demands put forth on a school district from the Department of Education requesting more</br></br>
transparency and accountability in handling school funds, it is critical to have the appropriate system in</br></br> 
place to capture the day-to-day funds flowing into the school system.</br></br>

Features and Highlights</br></br>

eSchool securely processes, reports and tracks the flow of money into your school system. With a user</br></br> 
friendly interface, easy to access reports and creative managing tools eSchool is at the top of its class!</br></br>

 eSchool Highlights and features</br></br>
• Designed to meet the needs of any school size</br></br>
• Create and manage student groups applying user-defined filters and sorting</br></br>
• Bill Generator, this self serve feature allows a department to create fees and dues</br></br>
• User-defined report specifications</br></br>
• Easily view and print reports with all payment information</br></br>
• Records all payments made by check, cash or online</br></br>
• History of student payment information</br></br>
• Ability to maintain a balance with transactional details</br></br>
• Import student lists</br></br>
• Accommodate post dated checks and/or prepayments</br></br>
• eSchool add-ons</br></br>

eSchool Benefits for the school system</br></br>
• Easily manage payment information flowing into the school system</br></br>
• Maintain and improve financial accountability</br></br>
• Simplify and streamline reconciliation of payment remittances</br></br>
• Accessible from anywhere (i.e. home computer)</br></br>
• Connects all District personnel</br></br>
• Sophisticated audit trail</br></br>
• Reduce receipt writing  -Lower operating and banking costs</br></br>

